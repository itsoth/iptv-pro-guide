export function t(langCode: string, key: string, replacements?: Record<string, string>): string {
  const lang = TRANSLATIONS_DATA[langCode] || TRANSLATIONS_DATA.en
  let str = lang[key] || TRANSLATIONS_DATA.en[key] || key
  if (replacements) {
    for (const [k, v] of Object.entries(replacements)) {
      str = str.replace(new RegExp('\\[' + k + '\\]', 'g'), v)
    }
  }
  return str
}

